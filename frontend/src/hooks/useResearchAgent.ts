import { useState, useRef, useCallback } from 'react'
import { AgentState, AgentStep, StepStatus, LogEntry, SSEFrame, UsageMetrics } from '../types/agent'

const INITIAL_STEP_STATUSES: Record<AgentStep, StepStatus> = {
  planner: 'idle',
  searcher: 'idle',
  summarizer: 'idle',
  critic: 'idle',
  report_generator: 'idle',
}

const INITIAL_STATE: AgentState = {
  status: 'idle',
  currentStep: null,
  stepStatuses: { ...INITIAL_STEP_STATUSES },
  subQuestions: [],
  completedQuestions: [],
  logs: [],
  finalReport: '',
  topic: '',
  retryCount: 0,
  total_tokens: 0,
  wordCount: 0,
}

function formatTimestamp(): string {
  const now = new Date()
  const hh = String(now.getHours()).padStart(2, '0')
  const mm = String(now.getMinutes()).padStart(2, '0')
  const ss = String(now.getSeconds()).padStart(2, '0')
  return `${hh}:${mm}:${ss}`
}

function createLog(message: string, type: LogEntry['type'] = 'info'): LogEntry {
  return {
    id: `${Date.now()}-${Math.random()}`,
    timestamp: formatTimestamp(),
    message,
    type,
  }
}

