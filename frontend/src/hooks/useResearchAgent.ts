export function useResearchAgent() {
  const [state, setState] = useState<AgentState>(INITIAL_STATE)
  const [mockMode, setMockMode] = useState(false)
  const cleanupRef = useRef<(() => void) | null>(null)
  const eventSourceRef = useRef<EventSource | null>(null)

  const processFrame = useCallback((frame: SSEFrame) => {
    setState(prev => {
      const next = { ...prev }

      switch (frame.type) {
        case 'step_start': {
          if (!frame.node) break
          const newStatuses = { ...prev.stepStatuses }
          if (prev.currentStep && prev.currentStep !== frame.node) {
            newStatuses[prev.currentStep] = 'completed'
          }
          newStatuses[frame.node] = 'active'
          next.stepStatuses = newStatuses
          next.currentStep = frame.node
          break
        }
        case 'step_complete': {
          if (!frame.node) break
          const newStatuses = { ...prev.stepStatuses }
          newStatuses[frame.node] = 'completed'
          next.stepStatuses = newStatuses
          if (prev.currentStep === frame.node) {
            next.currentStep = null
          }
          break
        }
        case 'questions': {
          if (Array.isArray(frame.data)) {
            next.subQuestions = frame.data as string[]
          }
          break
        }
        case 'log': {
          if (frame.data && typeof frame.data === 'object' && !Array.isArray(frame.data)) {
            const d = frame.data as { message?: string; type?: LogEntry['type'] }
            next.logs = [
              ...prev.logs,
              createLog(d.message || '', d.type || 'info'),
            ]
          }
          break
        }
        case 'usage': {
          if (frame.data && typeof frame.data === 'object' && !Array.isArray(frame.data)) {
            const d = frame.data as UsageMetrics
            next.total_tokens = d.total_tokens
          }
          break
        }
        case 'report_chunk': {
          if (typeof frame.data === 'string') {
            const newReport = prev.finalReport + frame.data
            next.finalReport = newReport
            next.wordCount = newReport.split(/\s+/).filter(Boolean).length
          }
          break
        }
        case 'done': {
          next.status = 'complete'
          next.currentStep = null
          if (frame.node) {
            const newStatuses = { ...prev.stepStatuses }
            newStatuses[frame.node] = 'completed'
            next.stepStatuses = newStatuses
          }
          next.logs = [
            ...prev.logs,
            createLog('Research complete. Report ready.', 'success'),
          ]
          break
        }
      }

      return next
    })
  }, [])

  const startResearch = useCallback((topic: string) => {
    if (cleanupRef.current) cleanupRef.current()
    if (eventSourceRef.current) eventSourceRef.current.close()

    setState({
      ...INITIAL_STATE,
      status: 'researching',
      topic,
      logs: [createLog(`Starting research on: "${topic}"`, 'info')],
    })

    if (mockMode) {
      cleanupRef.current = mockResearch(topic, processFrame)
    } else {
      const url = `http://localhost:8000/research/stream?topic=${encodeURIComponent(topic)}`
      const es = new EventSource(url)
      eventSourceRef.current = es

      es.onmessage = (event) => {
        try {
          const frame = JSON.parse(event.data) as SSEFrame
          processFrame(frame)
        } catch {
          // ignore parse errors
        }
      }

      es.onerror = () => {
        setState(prev => {
          if (prev.status === 'complete') {
            return prev
          }
          return {
            ...prev,
            status: 'error',
            logs: [...prev.logs, createLog('Connection error', 'error')],
          }
        })
        es.close()
      }

      cleanupRef.current = () => es.close()
    }
  }, [mockMode, processFrame])

  const stopResearch = useCallback(() => {
    if (cleanupRef.current) {
      cleanupRef.current()
      cleanupRef.current = null
    }
    if (eventSourceRef.current) {
      eventSourceRef.current.close()
      eventSourceRef.current = null
    }
    setState(prev => ({
      ...prev,
      status: 'idle',
      logs: [...prev.logs, createLog('Research stopped by user.', 'warning')],
    }))
  }, [])

  const resetResearch = useCallback(() => {
    if (cleanupRef.current) cleanupRef.current()
    if (eventSourceRef.current) eventSourceRef.current.close()
    setState(INITIAL_STATE)
  }, [])

  const toggleMockMode = useCallback(() => {
    setMockMode(prev => !prev)
  }, [])

  const markQuestionComplete = useCallback((question: string) => {
    setState(prev => ({
      ...prev,
      completedQuestions: [...prev.completedQuestions, question],
    }))
  }, [])

  return {
    state,
    mockMode,
    startResearch,
    stopResearch,
    resetResearch,
    toggleMockMode,
    markQuestionComplete,
  }
}
